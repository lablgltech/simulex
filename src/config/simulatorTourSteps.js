/** Отфильтровать шаги: skipIfNoElement — пропустить, если якоря нет в DOM. */
export function filterResolvableTourSteps(steps) {
  if (!Array.isArray(steps)) return [];
  return steps.filter((s) => {
    if (!s || !s.skipIfNoElement || !s.highlightId) return true;
    if (typeof document === 'undefined') return false;
    return !!document.querySelector(`[data-tutor-highlight="${s.highlightId}"]`);
  });
}

