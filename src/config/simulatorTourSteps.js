const TYPE_TO_STAGE_ID = {
  context: 'stage-1',
  position: 'stage-2',
  negotiation: 'stage-3',
  crisis: 'stage-4',
};

/** Шаг «кредиты» только если в DOM есть якорь (админ-метаданные). */
export const CREDITS_STEP = {
  id: 'hud-credits',
  title: 'Кредиты',
  body: 'Ресурс этапа: некоторые действия расходуют кредиты. Следите за балансом в шапке.',
  highlightId: 'credits',
  expandNav: true,
  skipIfNoElement: true,
};

export function getStageTourSteps(stageId, stageType) {
  if (stageId && STAGE_BY_ID[stageId]) return STAGE_BY_ID[stageId];
  const mapped = stageType && TYPE_TO_STAGE_ID[stageType];
  if (mapped && STAGE_BY_ID[mapped]) return STAGE_BY_ID[mapped];
  return [];
}

/** Только HUD (и опционально кредиты) — после приветствия, до тура экрана этапа. */
export function buildHudOnlyTourStepList(opts) {
  const { showTimer, includeCreditsStep } = opts;
  const hud = getHudTourSteps({ showTimer });
  const creditStep = includeCreditsStep ? [CREDITS_STEP] : [];
  return [...hud, ...creditStep];
}

/**
 * Собрать список шагов: HUD + опционально credits + этап (для совместимости / отладки).
 * @param {{ showTimer: boolean, stageId?: string, stageType?: string, includeCreditsStep?: boolean }} opts
 */
export function buildTourStepList(opts) {
  const { showTimer, stageId, stageType, includeCreditsStep } = opts;
  const hudOnly = buildHudOnlyTourStepList({ showTimer, includeCreditsStep });
  const stageSteps = getStageTourSteps(stageId, stageType);
  return [...hudOnly, ...stageSteps];
}
