/** Показывать полноэкранное приветствие только у кейса с таким числом этапов. */
export const INTRO_FULL_CASE_STAGE_COUNT = 4;

function getActualCasePayload(casePayload) {
  return casePayload?.case || casePayload || null;
}

function truncateIntroLine(s, maxLen) {
  const t = String(s || '')
    .replace(/\*\*/g, '')
    .replace(/\s+/g, ' ')
    .trim();
  if (t.length <= maxLen) return t;
  return `${t.slice(0, Math.max(0, maxLen - 1))}…`;
}

/** Конфиг полноэкранного приветствия хранится в data/case*.json: `case.simulator_intro`. */
export function getSimulatorIntroConfig(casePayload) {
  const actualCase = getActualCasePayload(casePayload);
  const cfg = actualCase?.simulator_intro;
  return cfg && typeof cfg === 'object' ? cfg : null;
}

/** Включено ли приветствие для данного кейса. */
export function shouldShowSimulatorIntro(casePayload) {
  const actualCase = getActualCasePayload(casePayload);
  const stages = actualCase?.stages;
  const cfg = getSimulatorIntroConfig(actualCase);
  if (!cfg || cfg.enabled === false) return false;
  const requiredStageCount = Number(cfg.required_stage_count || INTRO_FULL_CASE_STAGE_COUNT);
  return Array.isArray(stages) && stages.length === requiredStageCount;
}

/**
 * Убрать из подписи первый абзац, если он дословно совпадает с заголовком этапа (см. `st.title` у карточки).
 */
function stripDuplicateComicCaption(heading, raw) {
  const h = (heading || '').trim();
  if (!h || !raw) return raw;
  const s = String(raw);
  const parts = s.split(/\n\n+/);
  if (parts.length >= 2 && parts[0].trim() === h) {
    return parts.slice(1).join('\n\n').trim();
  }
  if (parts.length === 1 && s.trim() === h) {
    return '';
  }
  return raw;
}

/**
 * Кадры комикса на 3-м шаге: заголовок и подпись из конфига этапа (`intro`), картинки — из `imageSrcs` или плейсхолдер.
 */
export function buildIntroComicPanelsFromStages(stages, introConfig) {
  if (!Array.isArray(stages) || stages.length !== INTRO_FULL_CASE_STAGE_COUNT) return [];
  const comicConfig = introConfig?.part_comic;
  const imgs = comicConfig?.image_srcs;
  const captions = comicConfig?.caption_overrides;
  return stages.map((st, i) => {
    const heading = (st.title || `Этап ${i + 1}`).trim();
    const rawCaption = (Array.isArray(captions) && captions[i]) || st.intro || '';
    return {
      stageOrder: i + 1,
      heading,
      caption: truncateIntroLine(stripDuplicateComicCaption(heading, rawCaption), 320),
      imageSrc: (Array.isArray(imgs) && imgs[i]) || null,
    };
  });
}

