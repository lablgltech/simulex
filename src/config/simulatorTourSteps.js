/**
 * Устаревшие ключи пропуска тура (не читать для логики «показывать / не показывать»):
 * - глобальный на весь origin;
 * - v1: `_u{id}` без суффикса версии — после смены схемы все снова видят обучение, пока сами не отметят «не показывать».
 * - v2 → v3 (2026-04): принудительный сброс чекбокса «не показывать автотур» у всех после выкладки (новый ключ).
 */
export const SKIP_CASE_SIMULATOR_TOUR_KEY = 'simulex_skip_case_simulator_tour';

const SKIP_CASE_SIMULATOR_TOUR_KEY_V2 = 'simulex_skip_case_simulator_tour_v2';
const SKIP_CASE_SIMULATOR_TOUR_KEY_V3 = 'simulex_skip_case_simulator_tour_v3';

/** Удалить устаревшие флаги пропуска (глобальный, v1 и v2 на пользователя). Вызывать после входа и при /me. */
export function clearObsoleteSimulatorTourSkipKeys(userId) {
  try {
    if (typeof localStorage === 'undefined') return;
    localStorage.removeItem(SKIP_CASE_SIMULATOR_TOUR_KEY);
    if (userId != null && !Number.isNaN(Number(userId))) {
      const u = Number(userId);
      localStorage.removeItem(`${SKIP_CASE_SIMULATOR_TOUR_KEY}_u${u}`);
      localStorage.removeItem(`${SKIP_CASE_SIMULATOR_TOUR_KEY_V2}_u${u}`);
    }
  } catch (_) {
    /* ignore */
  }
}

/** Снимок сессии симуляции в localStorage раньше был общим на origin — вызывал подмешивание сессий между аккаунтами. */
export const LEGACY_ACTIVE_SESSION_LOCAL_KEY = 'simulex_active_session_v1';

