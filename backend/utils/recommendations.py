"""Генерация рекомендаций на основе LEXIC"""
from typing import Dict, List

def generate_recommendations(lexic: Dict[str, int]) -> List[str]:
    """Рекомендации по сырому LEXIC движка (L/E/X/I/C — см. подписи в UI отчёта)."""
    recs = []
    if lexic.get("L", 50) < 50:
        recs.append("Легитимность: усильте опору на процедуры, полномочия и правовые основания.")
    if lexic.get("E", 50) < 50:
        recs.append("Эффективность: планируйте время и приоритизируйте ключевые шаги сделки.")
    if lexic.get("X", 50) < 50:
        recs.append("Экспертиза: углубите разбор рисков и проверку условий по смыслу договора.")
    if lexic.get("I", 50) < 50:
        recs.append("Интересы компании: явнее балансируйте защиту позиции, риски и репутацию.")
    if lexic.get("C", 50) < 50:
        recs.append("Ясность: формулируйте условия понятнее для бизнеса и контрагента.")
    if not recs:
        recs.append("По всем параметрам LEXIC результат не ниже среднего; сохраняйте текущий уровень дисциплины.")
    return recs
